package com.example.francesco.mytravel.utils;

public class TripItem {

    public String name;
    public String start;
    public String end;
    public String id;
    public String img;

    public TripItem(String n, String s, String e, String i, String image) {
        name = n;
        start = s;
        end = e;
        id = i;
        img = image;
    }
}
